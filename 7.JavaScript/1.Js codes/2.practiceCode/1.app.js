// Condition statement ka matlab hota hai **aisa statement jo program ke flow ko decide karta hai based on kisi condition (true ya false)**.

// ### JavaScript me Condition Statements:

// 1. **if statement**
//    Agar condition true hai to code chalega.

//    ```js
//    let age = 18;

//    if (age >= 18) {
//        console.log("You are eligible to vote");
//    }
//    ```

// 2. **if...else statement**
//    Agar condition true hai to ek code chalega, nahi to doosra.

//    ```js
//    let age = 16;

//    if (age >= 18) {
//        console.log("You are eligible to vote");
//    } else {
//        console.log("You are not eligible to vote");
//    }
//    ```

// 3. **if...else if...else statement**
//    Multiple conditions check karne ke liye.

//    ```js
//    let marks = 75;

//    if (marks >= 90) {
//        console.log("Grade A");
//    } else if (marks >= 75) {
//        console.log("Grade B");
//    } else if (marks >= 50) {
//        console.log("Grade C");
//    } else {
//        console.log("Fail");
//    }
//    ```

// 4. **switch statement**
//    Jab ek variable ke multiple fixed values check karni ho.

//    let day = 3;

//    switch(day) {
//        case 1:
//            console.log("Monday");
//            break;
//        case 2:
//            console.log("Tuesday");
//            break;
//        case 3:
//            console.log("Wednesday");
//            break;
//        default:
//            console.log("Invalid day");
//    }
//    ```

