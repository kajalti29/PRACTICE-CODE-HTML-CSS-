// Unary operators
//1. Post-increment
let x = 5;
console.log(x++);  // 5 (पहले value use हुई)
console.log(x);    // 6 (फिर increment हुआ)

//2. pre-increment
let y = 5;
console.log(++y);  // 6 (पहले increment हुआ, फिर use हुआ)
console.log(y);    // 6

//3. Post-Decrement (x--) 
let a = 5;
console.log(a--);  // 5 (पहले value use हुई)
console.log(a);    // 4 (फिर decrement हुआ)

//4. Pre-Decrement (--x)
let b = 5;
console.log(--b);  // 4 (पहले decrement हुआ, फिर use हुआ)
console.log(b);    // 4
